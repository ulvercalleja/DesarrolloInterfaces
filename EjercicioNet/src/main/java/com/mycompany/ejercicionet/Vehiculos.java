    private void botonResetActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonResetActionPerformed
        int respuesta = JOptionPane.showConfirmDialog(null, "¿Está seguro que desea RESETEAR?",
                "OK_CANCEL_OPTION", JOptionPane.OK_CANCEL_OPTION,
                JOptionPane.INFORMATION_MESSAGE);
        if (respuesta == 0){
            grupoVehiculos.clearSelection();
            grupoCaracteristicas.clearSelection();
            checkSidecar.setEnabled(true);
            checkCapota.setEnabled(true);
            checkRemolque.setEnabled(true);
            grupoCombustible.clearSelection();
            cajaMarca.setText("");
            cajaModelo.setText("");
            txtNombre.setText("");
            JOptionPane.showMessageDialog(null, "Usted ha decidido resetear",
                    "INFORMATION_MESSAGE", JOptionPane.INFORMATION_MESSAGE);
        }
        
    }//GEN-LAST:event_botonResetActionPerformed

