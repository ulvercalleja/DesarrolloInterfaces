    private void botonSacarInfoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonSacarInfoActionPerformed
        DefaultTableModel modeloDatos = (DefaultTableModel)tblVehiculos.getModel();
        String vehiculo = "";
        if (checkCoche.isSelected()){
            vehiculo = "Coche"; 
        } else if (checkMoto.isSelected()){
            vehiculo = "Moto"; 
        } else if (checkCamion.isSelected()){
            vehiculo = "Camion"; 
        };
        String extra = "";
        if (checkRemolque.isSelected()){
            extra = "Remolque"; 
        } else if (checkCapota.isSelected()){
            extra = "Capota"; 
        } else if (checkSidecar.isSelected()){
            extra = "Sidecar"; 
        };
        String carburante = "";
        if (radioGasolina.isSelected()){
            carburante = "Gasolina"; 
        } else if (radioElectrico.isSelected()){
            carburante = "Electrico"; 
        } else if (radioDiesel.isSelected()){
            carburante = "Diesel"; 
        };
        
        modeloDatos.addRow(new Object[]{txtNombre.getText(),cajaMarca.getText(),cajaModelo.getText(),vehiculo,extra,carburante});
    }//GEN-LAST:event_botonSacarInfoActionPerformed

