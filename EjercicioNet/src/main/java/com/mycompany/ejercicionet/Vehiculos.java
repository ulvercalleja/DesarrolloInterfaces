    private void botonSacarInfoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_botonSacarInfoMouseClicked
        JOptionPane.showMessageDialog(null, "Modelo: " + cajaModelo.getText() + " Marca: " + cajaMarca.getText());
        
        if (checkCoche.isSelected()){
            JOptionPane.showMessageDialog(null, "Usted tiene un coche");
        } else if (checkMoto.isSelected()){
            JOptionPane.showMessageDialog(null, "Usted tiene una moto");
        } else if (checkCamion.isSelected()){
            JOptionPane.showMessageDialog(null, "Usted tiene un camion");}
        
        if (checkRemolque.isSelected()){
            JOptionPane.showMessageDialog(null, "Tambien seleccionado un remolque");
        } else if (checkCapota.isSelected()){
            JOptionPane.showMessageDialog(null, "Tambien seleccionado una capota");
        } else if (checkSidecar.isSelected()){JOptionPane.showMessageDialog(null, "Tambien seleccionado un sidecar");}
        
        if (radioGasolina.isSelected()){
            JOptionPane.showMessageDialog(null, "Combustible: Gasolina");
        } else if (radioDiesel.isSelected()){
            JOptionPane.showMessageDialog(null, "Combustible: Diesel");
        } else if (radioElectrico.isSelected()){JOptionPane.showMessageDialog(null, "Combustible: Electrico");}
        
        JOptionPane.showMessageDialog(null, "Color del vehículo: " + comboBoxColor.getSelectedItem().toString());
    }//GEN-LAST:event_botonSacarInfoMouseClicked

